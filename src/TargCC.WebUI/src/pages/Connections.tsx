/**
 * Connections Page
 * Full-page view for managing database connections
 */

import { useState } from 'react';
import {
  Box,
  Container,
  Paper,
  Typography,
  Button,
  List,
  ListItem,
  ListItemText,
  IconButton,
  CircularProgress,
  Chip,
} from '@mui/material';
import AddIcon from '@mui/icons-material/Add';
import EditIcon from '@mui/icons-material/Edit';
import DeleteIcon from '@mui/icons-material/Delete';
import CheckCircleIcon from '@mui/icons-material/CheckCircle';
import StorageIcon from '@mui/icons-material/Storage';
import { useConnection } from '../hooks/useConnection';
import { addConnection, updateConnection, deleteConnection, testConnection } from '../api/connectionApi';
import ConnectionFormDialog from '../components/connections/ConnectionFormDialog';
import type { ConnectionFormData } from '../components/connections/ConnectionForm';

export default function Connections() {
  const {
    connections,
    isLoading: loading,
    selectedConnection,
    setSelectedConnection,
    refreshConnections,
  } = useConnection();

  const [testing, setTesting] = useState<string | null>(null);
  const [dialogOpen, setDialogOpen] = useState(false);
  const [editingConnection, setEditingConnection] = useState<ConnectionFormData | undefined>(undefined);

  const handleTest = async (connectionString: string, id: string) => {
    setTesting(id);
    try {
      const isValid = await testConnection(connectionString);
      alert(isValid ? 'Connection successful! ✅' : 'Connection failed ❌');
    } catch (error) {
      alert('Connection test failed ❌');
    } finally {
      setTesting(null);
    }
  };

  const handleDelete = async (id: string) => {
    if (window.confirm('Are you sure you want to delete this connection?')) {
      try {
        await deleteConnection(id);
        if (selectedConnection?.id === id) {
          setSelectedConnection(null);
        }
        await refreshConnections();
      } catch (error) {
        alert('Failed to delete connection');
      }
    }
  };

  const handleSelect = (conn: typeof connections[0]) => {
    setSelectedConnection(conn);
  };

  const handleAdd = () => {
    setEditingConnection(undefined);
    setDialogOpen(true);
  };

  const handleEdit = (conn: typeof connections[0]) => {
    setEditingConnection({
      id: conn.id,
      name: conn.name,
      server: conn.server,
      database: conn.database,
      useIntegratedSecurity: conn.useIntegratedSecurity,
      username: conn.username,
    });
    setDialogOpen(true);
  };

  const handleSubmit = async (data: ConnectionFormData) => {
    try {
      if (data.id) {
        // Update existing connection - need to find the full connection object
        const existingConn = connections.find(c => c.id === data.id);
        if (existingConn) {
          await updateConnection({
            ...existingConn,
            name: data.name,
            server: data.server,
            database: data.database,
            useIntegratedSecurity: data.useIntegratedSecurity,
            username: data.username,
          });
        }
      } else {
        // Add new connection - connectionString will be generated by the API
        const newConn = await addConnection({
          name: data.name,
          server: data.server,
          database: data.database,
          useIntegratedSecurity: data.useIntegratedSecurity,
          username: data.username,
          connectionString: '', // Server will generate this
        });
        // Auto-select the first connection if none is selected
        if (!selectedConnection) {
          setSelectedConnection(newConn);
        }
      }
      await refreshConnections();
    } catch (error) {
      console.error('Failed to save connection:', error);
      alert('Failed to save connection');
      throw error; // Re-throw to prevent dialog from closing
    }
  };

  const handleCloseDialog = () => {
    setDialogOpen(false);
    setEditingConnection(undefined);
  };

  const formatLastUsed = (date: Date | string): string => {
    const d = typeof date === 'string' ? new Date(date) : date;
    const now = new Date();
    const diffMs = now.getTime() - d.getTime();
    const diffMins = Math.floor(diffMs / 60000);

    if (diffMins < 1) return 'just now';
    if (diffMins < 60) return `${diffMins} minutes ago`;
    const diffHours = Math.floor(diffMins / 60);
    if (diffHours < 24) return `${diffHours} hours ago`;
    const diffDays = Math.floor(diffHours / 24);
    return `${diffDays} days ago`;
  };

  return (
    <Container maxWidth="lg" sx={{ mt: 4, mb: 4 }}>
      <Paper sx={{ p: 3 }}>
        <Box sx={{ display: 'flex', alignItems: 'center', justifyContent: 'space-between', mb: 3 }}>
          <Typography variant="h5" component="h1">
            Database Connections
          </Typography>
          <Button
            startIcon={<AddIcon />}
            variant="contained"
            onClick={handleAdd}
          >
            Add Connection
          </Button>
        </Box>

        {loading ? (
          <Box sx={{ display: 'flex', justifyContent: 'center', p: 4 }}>
            <CircularProgress />
          </Box>
        ) : connections.length === 0 ? (
          <Box sx={{ textAlign: 'center', p: 4 }}>
            <StorageIcon sx={{ fontSize: 64, color: 'text.secondary', mb: 2 }} />
            <Typography variant="h6" color="text.secondary" gutterBottom>
              No connections yet
            </Typography>
            <Typography color="text.secondary" sx={{ mb: 2 }}>
              Add your first database connection to get started
            </Typography>
          </Box>
        ) : (
          <List>
            {connections.map((conn) => (
              <ListItem
                key={conn.id}
                sx={{
                  border: 1,
                  borderColor: selectedConnection?.id === conn.id ? 'primary.main' : 'divider',
                  borderWidth: selectedConnection?.id === conn.id ? 2 : 1,
                  borderRadius: 1,
                  mb: 2,
                  bgcolor: selectedConnection?.id === conn.id ? 'action.selected' : 'background.paper',
                  cursor: 'pointer',
                }}
                onClick={() => handleSelect(conn)}
              >
                <StorageIcon sx={{ mr: 2, color: 'primary.main', fontSize: 40 }} />
                <ListItemText
                  primary={
                    <Box sx={{ display: 'flex', alignItems: 'center', gap: 1 }}>
                      <Typography variant="h6">{conn.name}</Typography>
                      {selectedConnection?.id === conn.id && (
                        <Chip label="Selected" size="small" color="primary" />
                      )}
                      {conn.useIntegratedSecurity && (
                        <Chip label="Integrated Security" size="small" color="primary" variant="outlined" />
                      )}
                    </Box>
                  }
                  secondary={
                    <Box component="span" sx={{ display: 'block' }}>
                      <Typography component="span" variant="body2" color="text.secondary" sx={{ display: 'block' }}>
                        {conn.server} / {conn.database}
                      </Typography>
                      <Typography component="span" variant="caption" color="text.secondary" sx={{ display: 'block' }}>
                        Last used: {formatLastUsed(conn.lastUsed)}
                      </Typography>
                    </Box>
                  }
                />
                <Box sx={{ display: 'flex', gap: 1 }}>
                  <IconButton
                    color="success"
                    onClick={(e) => {
                      e.stopPropagation();
                      handleTest(conn.connectionString, conn.id);
                    }}
                    disabled={testing === conn.id}
                    title="Test Connection"
                  >
                    {testing === conn.id ? (
                      <CircularProgress size={24} />
                    ) : (
                      <CheckCircleIcon />
                    )}
                  </IconButton>
                  <IconButton
                    color="primary"
                    onClick={(e) => {
                      e.stopPropagation();
                      handleEdit(conn);
                    }}
                    title="Edit Connection"
                  >
                    <EditIcon />
                  </IconButton>
                  <IconButton
                    color="error"
                    onClick={(e) => {
                      e.stopPropagation();
                      handleDelete(conn.id);
                    }}
                    title="Delete Connection"
                  >
                    <DeleteIcon />
                  </IconButton>
                </Box>
              </ListItem>
            ))}
          </List>
        )}
      </Paper>

      <ConnectionFormDialog
        open={dialogOpen}
        onClose={handleCloseDialog}
        initialData={editingConnection}
        onSubmit={handleSubmit}
        onTestConnection={testConnection}
      />
    </Container>
  );
}
